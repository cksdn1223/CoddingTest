package ct.week01;
/*
자료형
기본 int float
long 은 뒤에 L
float은 뒤에 F
 */
public class Ex02 {
    public static void main(String[] args) {
        byte b = 0; // 1byte
        char c1 = 'A', c2 = 65; // 2byte
        short s = 100;  // 2byte
        int i = 65; // 4byte
        long l = 10000000000L;   // 8byte

        float f = 3.141592f; // 4byte
        double d = 3.141592;    // 8byte

        boolean t = true;   // 1bit(1byte)
    }
}
